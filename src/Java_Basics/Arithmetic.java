package Java_Basics;




public class Arithmetic {
    public static void main(String[] args) {
        int A = 5;
        int B = 4;

        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);

        System.out.println(8-4+13); // 17
        System.out.println((9*9+1));// 82

    }
}
