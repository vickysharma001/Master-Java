public class While {
    public static void main(String[] args) {

        int rows = 1 ;

        while (rows <= 10){

            int column = 1 ;

            while (column <= rows){
                System.out.print(" &");
                column++ ;
            }
            System.out.println();
            rows++ ;
        }
    }
}











